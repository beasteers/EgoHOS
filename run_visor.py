def visor_to_cvsegments(segs):
    segs = [np.asarray(x)[:, None] for x in segs]
    assert all(x.shape[-2:]==(1,2) for x in segs)
    return tuple(segs)

def draw_segments_on_image(image, segments, class_ids, scale=None):
    output_mask = np.zeros(image.shape, dtype=np.uint8)
    for i, segment in enumerate(segments):
        if scale is not None:
            segment = [(s*scale).astype(int) for s in segment]
        cv2.drawContours(output_mask, segment, -1, colors[class_ids[i] % len(colors)], -1)
    return output_mask

def draw_sam_masks(anns, shape):
    img = np.zeros(shape, dtype=np.uint8)
    for ann in sorted(anns, key=(lambda x: x['area']), reverse=True):
        img[ann['segmentation']] = (np.random.random(3)*255).astype(np.uint8)
    return img

